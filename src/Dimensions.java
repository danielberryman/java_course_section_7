public class Dimensions {

    private int width;
    private int height;
    private int depth;
    private int length;

    public Dimensions(int width, int height, int depth, int length) {
        this.width = width;
        this.height = height;
        this.depth = depth;
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getDepth() {
        return depth;
    }

    public int getLength() {
        return length;
    }
}
